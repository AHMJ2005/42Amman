char	*ft_strcat(char *dest, char *src)
{
	char	*d;

	d = dest;
	while (*d)
		d++;
	*d = *src;
	src++;
	while (*d)
	{
		d++;
		*d = *src;
		src++;
	}
	return (dest);
}
